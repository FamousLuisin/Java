package streams;

public class Produto {

	final String nome;
	final Double valor;
	final Double desconto;
	final Boolean freteGratis;
	
	public Produto(String nome, Double valor, Double desconto, Boolean freteGratis) {
		this.nome = nome;
		this.valor = valor;
		this.desconto = desconto;
		this.freteGratis = freteGratis;
	}
	
	
}
